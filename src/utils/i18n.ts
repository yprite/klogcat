export function t(language: Language | undefined, text: string, vars: Record<string, string | number> = {}) {
  const template = language === 'ko' ? ko[text] ?? text : text
  return Object.entries(vars).reduce((value, [key, replacement]) => value.replaceAll(`{${key}}`, String(replacement)), template)
}

export function translatePhase(language: Language | undefined, phase: string) {
  if (language !== 'ko') return phase
  if (phase === 'Running') return '실행 중'
  if (phase === 'Pending') return '대기 중'
  if (phase === 'Failed') return '실패'
  return phase
}
